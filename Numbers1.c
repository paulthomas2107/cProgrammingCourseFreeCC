#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{

    int num = 6;
    printf("%d\n", num);

    float f = 3.14;
    printf("%f\n", f);

    printf("%f\n", pow(4, 3));
    printf("%f\n", sqrt(16));
    printf("%f\n", ceil(36.4)); // round up
    printf("%f\n", floor(36.4)); // round down

    return 0;
}